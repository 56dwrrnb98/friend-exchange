    user_id,
    market_id,
    amount,
    'refund',
    'Market voided and refunded'
  from public.market_payouts
  where market_id = p_market_id;

  select coalesce(sum(amount), 0)
  into v_refund_total
  from public.market_payouts
  where market_id = p_market_id;

