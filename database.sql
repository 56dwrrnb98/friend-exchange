create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  display_name text not null,
  balance bigint not null default 1000,
  is_admin boolean not null default false,
  created_at timestamptz not null default now(),

  constraint profiles_display_name_length
    check (char_length(btrim(display_name)) between 2 and 32),
  constraint profiles_balance_nonnegative
    check (balance >= 0)
);

create table if not exists public.markets (
  id bigint generated by default as identity primary key,
  creator_id uuid not null references public.profiles(id) on delete restrict,
  question text not null,
  description text,
  closes_at timestamptz not null,
  status text not null default 'open',
  winning_outcome_id bigint,
  created_at timestamptz not null default now(),
  resolved_at timestamptz,

  constraint markets_question_length
    check (char_length(btrim(question)) between 5 and 180),
  constraint markets_description_length
    check (description is null or char_length(description) <= 600),
  constraint markets_status_valid
    check (status in ('open', 'resolved', 'void'))
);

create table if not exists public.outcomes (
  id bigint generated by default as identity primary key,
  market_id bigint not null references public.markets(id) on delete cascade,
  label text not null,
  sort_order integer not null default 0,
  seed_points bigint not null default 25,
  created_at timestamptz not null default now(),

  constraint outcomes_label_length
    check (char_length(btrim(label)) between 1 and 80),
  constraint outcomes_seed_points_nonnegative
    check (seed_points >= 0),
  constraint outcomes_id_market_unique
    unique (id, market_id)
);

-- Add the market → winning outcome relationship after outcomes exists.
do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'markets_winning_outcome_fkey'
      and conrelid = 'public.markets'::regclass
  ) then
    alter table public.markets
      add constraint markets_winning_outcome_fkey
      foreign key (winning_outcome_id)
      references public.outcomes(id)
      on delete restrict;
  end if;
end;
$$;

create table if not exists public.predictions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete restrict,
  market_id bigint not null,
  outcome_id bigint not null,
  amount bigint not null,
  created_at timestamptz not null default now(),

  constraint predictions_amount_positive
    check (amount > 0),
  constraint predictions_outcome_market_fkey
    foreign key (outcome_id, market_id)
    references public.outcomes(id, market_id)
    on delete restrict
);

create table if not exists public.market_payouts (
  market_id bigint not null references public.markets(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete restrict,
  amount bigint not null,
  kind text not null,
  created_at timestamptz not null default now(),

  primary key (market_id, user_id),
  constraint market_payouts_amount_nonnegative
    check (amount >= 0),
  constraint market_payouts_kind_valid
    check (kind in ('winner', 'no_winner_refund', 'void_refund'))
);

create table if not exists public.point_transactions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  market_id bigint references public.markets(id) on delete set null,
  amount bigint not null,
  kind text not null,
  note text,
  created_at timestamptz not null default now(),

  constraint point_transactions_amount_nonzero
    check (amount <> 0),
  constraint point_transactions_kind_valid
    check (kind in ('initial_grant', 'prediction', 'payout', 'refund', 'admin_adjustment'))
);

alter table public.point_transactions
  add column if not exists allowance_period date;

alter table public.point_transactions
  drop constraint if exists point_transactions_kind_valid;

alter table public.point_transactions
  add constraint point_transactions_kind_valid
    check (
      kind in (
        'initial_grant',
        'prediction',
        'payout',
        'refund',
        'admin_adjustment',
        'monthly_allowance'
      )
    );

alter table public.point_transactions
  drop constraint if exists point_transactions_allowance_period_valid;

alter table public.point_transactions
  add constraint point_transactions_allowance_period_valid
    check (
      (
        kind = 'monthly_allowance'
        and allowance_period is not null
        and extract(day from allowance_period) = 1
        and amount = 100
        and market_id is null
      )
      or (
        kind <> 'monthly_allowance'
        and allowance_period is null
      )
    );

