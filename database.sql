  set
    status = 'void',
    winning_outcome_id = null,
    resolved_at = now()
  where id = p_market_id;

  return jsonb_build_object(
    'market_id', p_market_id,
    'refund_total', v_refund_total
  );
end;
$$;

create or replace function public.award_points(
  p_user_id uuid,
  p_amount bigint,
  p_note text default 'Manual admin adjustment'
)
returns bigint
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_admin_id uuid := auth.uid();
  v_is_admin boolean := false;
  v_current_balance bigint;
  v_new_balance bigint;
begin
  if v_admin_id is null then
    raise exception 'You must be signed in.';
  end if;

  select coalesce(is_admin, false)
  into v_is_admin
  from public.profiles
  where id = v_admin_id;

  if not v_is_admin then
    raise exception 'Only administrators can adjust point balances.';
  end if;

  if p_amount is null or p_amount = 0 then
    raise exception 'The adjustment must be a non-zero number of points.';
  end if;

  select balance
  into v_current_balance
  from public.profiles
  where id = p_user_id
  for update;

  if not found then
    raise exception 'Target profile not found.';
  end if;

  v_new_balance := v_current_balance + p_amount;
  if v_new_balance < 0 then
    raise exception 'This adjustment would make the balance negative.';
  end if;

  update public.profiles
  set balance = v_new_balance
  where id = p_user_id;

  insert into public.point_transactions (
    user_id,
    amount,
    kind,
    note
  )
  values (
    p_user_id,
    p_amount,
    'admin_adjustment',
    left(coalesce(nullif(btrim(p_note), ''), 'Manual admin adjustment'), 200)
  );

  return v_new_balance;
end;
$$;

create or replace function public.grant_monthly_allowance(
  p_allowance_period date default (
    date_trunc('month', timezone('America/Chicago', now()))::date
  )
)
returns table (
  grant_period date,
  recipient_count bigint,
  points_granted bigint
)
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_period_start timestamp with time zone;
begin
  if p_allowance_period is null
     or extract(day from p_allowance_period) <> 1 then
    raise exception 'Allowance period must be the first day of a month.';
  end if;

  v_period_start :=
    p_allowance_period::timestamp at time zone 'America/Chicago';

  return query
  with eligible_accounts as (
    select profile.id
    from public.profiles as profile
    join auth.users as auth_user
      on auth_user.id = profile.id
    where auth_user.last_sign_in_at >= v_period_start - interval '90 days'
      and auth_user.last_sign_in_at <= now()
  ),
  new_allowances as (
    insert into public.point_transactions (
      user_id,
      amount,
      kind,
      note,
      allowance_period
    )
    select
      eligible.id,
      100,
      'monthly_allowance',
      'Monthly active-trader allowance',
      p_allowance_period
    from eligible_accounts as eligible
    on conflict (user_id, allowance_period)
      where kind = 'monthly_allowance'
      do nothing
    returning user_id, amount
  ),
  credited_accounts as (
    update public.profiles as profile
    set balance = profile.balance + allowance.amount
    from new_allowances as allowance
    where profile.id = allowance.user_id
    returning allowance.amount
  )
  select
    p_allowance_period,
    count(*)::bigint,
    coalesce(sum(credited.amount), 0)::bigint
  from credited_accounts as credited;
end;
$$;

