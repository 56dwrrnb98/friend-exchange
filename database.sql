create or replace function public.update_display_name(
  p_display_name text
)
returns void
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_user_id uuid := auth.uid();
  v_name text := btrim(coalesce(p_display_name, ''));
begin
  if v_user_id is null then
    raise exception 'You must be signed in.';
  end if;

  if char_length(v_name) < 2 or char_length(v_name) > 32 then
    raise exception 'Display names must be between 2 and 32 characters.';
  end if;

  update public.profiles
  set display_name = v_name
  where id = v_user_id;

  if not found then
    raise exception 'Profile not found.';
  end if;
end;
$$;

create or replace function public.create_market(
  p_question text,
  p_description text,
  p_closes_at timestamptz,
  p_outcome_labels text[]
)
returns bigint
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_user_id uuid := auth.uid();
  v_market_id bigint;
  v_question text := btrim(coalesce(p_question, ''));
  v_description text := nullif(btrim(coalesce(p_description, '')), '');
  v_outcome_count integer;
begin
  if v_user_id is null then
    raise exception 'You must be signed in.';
  end if;

  if char_length(v_question) < 5 or char_length(v_question) > 180 then
    raise exception 'Questions must be between 5 and 180 characters.';
  end if;

  if v_description is not null and char_length(v_description) > 600 then
    raise exception 'Details cannot exceed 600 characters.';
  end if;

  if p_closes_at is null or p_closes_at <= now() then
    raise exception 'The closing time must be in the future.';
  end if;

  v_outcome_count := coalesce(cardinality(p_outcome_labels), 0);
  if v_outcome_count < 2 or v_outcome_count > 10 then
    raise exception 'Markets must have between 2 and 10 outcomes.';
  end if;

  if exists (
    select 1
    from unnest(p_outcome_labels) as item(label)
    where char_length(btrim(coalesce(label, ''))) < 1
       or char_length(btrim(label)) > 80
  ) then
    raise exception 'Each outcome must be between 1 and 80 characters.';
  end if;

  if (
    select count(*) <> count(distinct lower(btrim(label)))
    from unnest(p_outcome_labels) as item(label)
  ) then
    raise exception 'Outcome names must be unique.';
  end if;

  insert into public.markets (
    creator_id,
    question,
    description,
    closes_at
  )
  values (
    v_user_id,
    v_question,
    v_description,
    p_closes_at
  )
  returning id into v_market_id;

  insert into public.outcomes (market_id, label, sort_order, seed_points)
  select
    v_market_id,
    btrim(item.label),
    item.ordinality::integer - 1,
    25
  from unnest(p_outcome_labels) with ordinality as item(label, ordinality);

  return v_market_id;
end;
$$;

create or replace function public.place_prediction(
  p_market_id bigint,
  p_outcome_id bigint,
  p_amount bigint
)
returns bigint
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_user_id uuid := auth.uid();
  v_profile public.profiles%rowtype;
  v_market public.markets%rowtype;
  v_outcome_market_id bigint;
  v_prediction_id bigint;
begin
  if v_user_id is null then
    raise exception 'You must be signed in.';
  end if;

  if p_amount is null or p_amount < 1 then
    raise exception 'Prediction amount must be at least 1 point.';
  end if;

  select *
  into v_profile
  from public.profiles
  where id = v_user_id
  for update;

  if not found then
    raise exception 'Profile not found.';
  end if;

  if v_profile.balance < p_amount then
    raise exception 'You do not have enough available points.';
  end if;

  select *
  into v_market
  from public.markets
  where id = p_market_id
  for update;

  if not found then
    raise exception 'Market not found.';
  end if;

  if v_market.status <> 'open' then
    raise exception 'This market is no longer open.';
  end if;

  if now() >= v_market.closes_at then
    raise exception 'Predictions are closed for this market.';
  end if;

  select market_id
  into v_outcome_market_id
  from public.outcomes
  where id = p_outcome_id;

  if not found or v_outcome_market_id <> p_market_id then
    raise exception 'That outcome does not belong to this market.';
  end if;

  update public.profiles
  set balance = balance - p_amount
  where id = v_user_id;

  insert into public.predictions (
    user_id,
    market_id,
    outcome_id,
    amount
  )
  values (
    v_user_id,
    p_market_id,
    p_outcome_id,
    p_amount
  )
  returning id into v_prediction_id;

  insert into public.point_transactions (
    user_id,
    market_id,
    amount,
    kind,
    note
  )
  values (
    v_user_id,
    p_market_id,
    -p_amount,
    'prediction',
    'Prediction placed'
  );

  return v_prediction_id;
end;
$$;

create or replace function public.resolve_market(
  p_market_id bigint,
  p_winning_outcome_id bigint
)
returns jsonb
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_user_id uuid := auth.uid();
  v_market public.markets%rowtype;
  v_is_admin boolean := false;
  v_outcome_market_id bigint;
  v_total_pool bigint := 0;
  v_winning_pool bigint := 0;
  v_payout_total bigint := 0;
begin
  if v_user_id is null then
    raise exception 'You must be signed in.';
  end if;

  select coalesce(is_admin, false)
  into v_is_admin
  from public.profiles
  where id = v_user_id;

  select *
  into v_market
  from public.markets
  where id = p_market_id
  for update;

  if not found then
    raise exception 'Market not found.';
  end if;

  if v_market.creator_id <> v_user_id and not v_is_admin then
    raise exception 'Only the market creator or an administrator can resolve this market.';
  end if;

  if v_market.status <> 'open' then
    raise exception 'This market has already been resolved or voided.';
  end if;

  if now() < v_market.closes_at and not v_is_admin then
    raise exception 'The market cannot be resolved before its closing time.';
  end if;

  select market_id
  into v_outcome_market_id
  from public.outcomes
  where id = p_winning_outcome_id;

  if not found or v_outcome_market_id <> p_market_id then
    raise exception 'That outcome does not belong to this market.';
  end if;

  select coalesce(sum(amount), 0)
  into v_total_pool
  from public.predictions
  where market_id = p_market_id;

  select coalesce(sum(amount), 0)
  into v_winning_pool
  from public.predictions
  where market_id = p_market_id
    and outcome_id = p_winning_outcome_id;

  -- Nobody predicted anything: resolve cleanly with no payouts.
  if v_total_pool = 0 then
    update public.markets
    set
      status = 'resolved',
      winning_outcome_id = p_winning_outcome_id,
      resolved_at = now()
    where id = p_market_id;

    return jsonb_build_object(
      'market_id', p_market_id,
      'total_pool', 0,
      'winning_pool', 0,
      'payout_total', 0,
      'refunded', false
    );
  end if;

  -- If nobody backed the real winner, refund every participant.
  if v_winning_pool = 0 then
    insert into public.market_payouts (market_id, user_id, amount, kind)
    select
      p_market_id,
      user_id,
      sum(amount)::bigint,
      'no_winner_refund'
    from public.predictions
    where market_id = p_market_id
    group by user_id;

    update public.profiles as profile
    set balance = profile.balance + payout.amount
    from public.market_payouts as payout
    where payout.market_id = p_market_id
      and payout.user_id = profile.id;

    insert into public.point_transactions (
      user_id,
      market_id,
      amount,
      kind,
      note
    )
    select
      user_id,
      market_id,
      amount,
      'refund',
      'Refunded because nobody selected the winning outcome'
    from public.market_payouts
    where market_id = p_market_id;

    update public.markets
    set
      status = 'resolved',
      winning_outcome_id = p_winning_outcome_id,
      resolved_at = now()
    where id = p_market_id;

    return jsonb_build_object(
      'market_id', p_market_id,
      'total_pool', v_total_pool,
      'winning_pool', 0,
      'payout_total', v_total_pool,
      'refunded', true
    );
  end if;

  -- Distribute the entire pool among winning users in proportion to stake.
  -- Integer rounding leftovers are assigned by largest fractional remainder,
  -- ensuring that the full pool is distributed exactly.
  with stakes as (
    select
      user_id,
      sum(amount)::bigint as stake
    from public.predictions
    where market_id = p_market_id
      and outcome_id = p_winning_outcome_id
    group by user_id
  ),
  raw_payouts as (
    select
      user_id,
      stake,
      (stake::numeric * v_total_pool::numeric / v_winning_pool::numeric) as exact_payout
    from stakes
  ),
  rounded as (
    select
      user_id,
      floor(exact_payout)::bigint as base_payout,
      exact_payout - floor(exact_payout) as fractional_remainder
    from raw_payouts
  ),
  ranked as (
    select
      user_id,
      base_payout,
      row_number() over (order by fractional_remainder desc, user_id) as remainder_rank,
      (v_total_pool - sum(base_payout) over ())::bigint as leftover_points
    from rounded
  )
  insert into public.market_payouts (market_id, user_id, amount, kind)
  select
    p_market_id,
    user_id,
    base_payout + case when remainder_rank <= leftover_points then 1 else 0 end,
    'winner'
  from ranked;

  update public.profiles as profile
  set balance = profile.balance + payout.amount
  from public.market_payouts as payout
  where payout.market_id = p_market_id
    and payout.user_id = profile.id;

  insert into public.point_transactions (
    user_id,
    market_id,
    amount,
    kind,
    note
  )
  select
    user_id,
    market_id,
    amount,
    'payout',
    'Winning market payout'
  from public.market_payouts
  where market_id = p_market_id;

  select coalesce(sum(amount), 0)
  into v_payout_total
  from public.market_payouts
  where market_id = p_market_id;

  if v_payout_total <> v_total_pool then
    raise exception 'Payout calculation failed to distribute the full pool.';
  end if;

  update public.markets
  set
    status = 'resolved',
    winning_outcome_id = p_winning_outcome_id,
    resolved_at = now()
  where id = p_market_id;

  return jsonb_build_object(
    'market_id', p_market_id,
    'total_pool', v_total_pool,
    'winning_pool', v_winning_pool,
    'payout_total', v_payout_total,
    'refunded', false
  );
end;
$$;

