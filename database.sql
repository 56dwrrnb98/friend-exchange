create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_display_name text;
begin
  v_display_name := btrim(coalesce(new.raw_user_meta_data ->> 'display_name', ''));

  if char_length(v_display_name) < 2 or char_length(v_display_name) > 32 then
    v_display_name := 'Trader ' || left(new.id::text, 6);
  end if;

  insert into public.profiles (id, display_name, balance)
  values (new.id, v_display_name, 1000)
  on conflict (id) do nothing;

  insert into public.point_transactions (user_id, amount, kind, note)
  values (new.id, 1000, 'initial_grant', 'Welcome grant')
  on conflict do nothing;

  return new;
end;
$$;

-- Recreating the trigger is safe and ensures it points to the current function.
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

